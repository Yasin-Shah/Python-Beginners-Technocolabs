s1={10,"Hello",30,False,67.45,None}
s2={10,"Hello",30,False,67.45,45+23j}
print(s1)
print(s2)

# Operations on Set

print("Difference : ",s1-s2)
print("Difference : ",s2-s1)

print("Union : ",s1|s2)
print("Union : ",s2|s1)

print("Intersection : ",s1&s2)
print("Intersection : ",s2&s1)

print("Symmetric_ Difference: ",s1^s2)
print("Symmetric_ Difference : ",s2^s1)



